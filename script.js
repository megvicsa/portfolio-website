function animateCountUp(element, target, duration = 1000) {
    const startTime = performance.now();

    function update(currentTime) {
        const elapsed = currentTime - startTime;
        const progress = Math.min(elapsed / duration, 1);
        const value = Math.floor(progress * target);

        element.textContent = value;

        if (progress < 1) {
            requestAnimationFrame(update);
        } else {
            element.textContent = target;
        }
    }

    requestAnimationFrame(update);
}

// Initialize counting animation when page loads
document.addEventListener('DOMContentLoaded', () => {
    const stats = document.querySelectorAll('.count-up');
    
    // Create an observer to trigger animation when stats come into view
    const statsObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const stat = entry.target;
                const target = parseInt(stat.getAttribute('data-target'), 10);
                
                // Only animate if not already animated
                if (!stat.classList.contains('animated')) {
                    stat.classList.add('animated');
                    animateCountUp(stat, target, 2000); // 2 second duration
                }
            }
        });
    }, { threshold: 0.5 });

    // Observe each stat element
    stats.forEach(stat => {
        statsObserver.observe(stat);
    });
});


